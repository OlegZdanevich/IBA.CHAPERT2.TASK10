package com.company.Logic.IndexAndElement;

public class IndexAndElement {
    private int index;
    private double element;
    public IndexAndElement(int index, double element)
    {
        this.element=element;
        this.index=index;
    }

    public double getElement() {
        return element;
    }

    public int getIndex() {
        return index;
    }

}
